		<h1>2. Кликбейт </h1>

		<p>Злоумышленники тратят много сил на создание ярких заголовков, по которым хочется перейти. Они должны быть такими, чтобы устоять было невозможно: заманчивыми и невероятными, но при этом правдоподобными. Чем больше у злоумышленников ваших данных, тем проще им создать кликбейтный заголовок, который вас привлечет. Если вы на него нажмете, то вас смогут перенаправить куда угодно.

		Это может быть страницей входа на сайт через аккаунт на Facebook или ВКонтакте. Вас может смутить тот факт, что нужно заново выполнить вход, но вы можете просто сделать это машинально. Они надеются, что вы сделаете это, ведь если вы все-таки введете учетные данные для входа, они завладеют страницей.</p>

		<hr class = "line">

		<h1>3. Просьбы одолжить денег</h1>

		Друг просит в долг? Взломав чью-то учетную запись, злоумышленники первым делом пытаются разослать сообщения друзьям жертвы со срочной просьбой об одолжении денег в надежде найти сочувствующего друга, прежде чем обман будет раскрыт.

		Получив подобную просьбу в социальных сетях или по электронной почте, всегда уточняйте у знакомых лично, не взломан ли его аккаунт.

		<hr class = "line">

		<h1>4. Сокращенные URL-адреса</h1>

		<p>Сокращенные URL-адреса выглядят короткими и симпатичными, начинаясь, например, с bit.ly (популярный сервис для сокращения ссылок), но злоумышленники используют их для скрытия подозрительных сайтов, распространяя подобные ссылки в социальных сетях и мотивируя перейти по ним.

		Такие сокращенные URL-адреса могут маскироваться под официальные сайты, поэтому, прежде чем переходить по ним, проверьте эти адреса с помощью сервисов вроде CheckShortURL на наличие вредоносных программ.</p>

		<hr class = "line">

		<h1>5. Подозрительные запросы на добавление в друзья</h1>

		<p>Да, это кажется очевидным, но не упомянуть этот пункт нельзя. Получив запрос на добавление в друзья, всегда спрашивайте себя, зачем это нужно другому пользователю. Если причина вам непонятна, а страница вызывает подозрения, можно игнорировать подобный запрос.</p>

		<hr class = "line">

		<h1>6. Запросы от людей, которые уже были в списке друзей</h1>

		<p>Вы получаете запрос на добавление в друзья от пользователя, который уже есть в вашем списке контактов, легко объясняете это как его новую страницу или странный сбой в системе и добавляете пользователя. Это ведь ваш друг. На деле это не всегда так. Это прием социальной инженерии в ее самом ужасном проявлении, рассчитанный на то, что вы сами без задней мысли добавите мошенника и будете считать, что на другом конце — ваш знакомый.</p>

