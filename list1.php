		<h1>1. Опросы, тесты и конкурсы для сбора данных</h1>

		<p>Сегодня мы часто можем увидеть призывы пройти платный опрос, по итогу которого нужно лишь оставить данные банковской карты, чтобы оплатить небольшую комиссию за получение вознаграждения. Это мошенничество. Относитесь к подобным вещам скептически, даже если они рекламируются в социальных сетях. Иногда подобная реклама по ошибке проходит модерацию, и вы сможете оградить кого-нибудь от опасности, пожаловавшись на подобную рекламу.

		В многочисленных «шуточных» тестах также могут использоваться хитрые уловки, заставляющие участников поделиться определенной информацией. Вам может казаться это забавным: что такого в том, чтобы поделиться неважными данными, такими как дата рождения и место проживания, но скрытые мотивы организаторов-мошенников перечеркивают все удовольствие от прохождения таких тестов. На самом деле, таким образом злоумышленники выманивают у пользователей информацию о них и их друзьях. Не переходите по ссылке на такие тесты и, конечно, не делитесь ими. </p>

